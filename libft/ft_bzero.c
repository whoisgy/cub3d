#include "libft.h"
#include <stdlib.h>

void	ft_bzero(void *str, size_t n)
{
	size_t	i;

	i = 0;
	if (n > 0)
	{
		while (i < n)
		{
			*(char *)str = 0;
			str++;
			i++;
		}
	}
}
